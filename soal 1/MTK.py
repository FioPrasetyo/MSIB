class Matematika:
    def __init__(self, a, b):
        self.a = a
        self.b = b

    def penjumlahan(self):
        return self.a + self.b

    def pengurangan(self):
        return self.a - self.b

    def perkalian(self):
        return self.a * self.b

    def pembagian(self):
        return self.a / self.b